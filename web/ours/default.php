<h1> Madrid et la guerre civile </h1>
<p>Durant la guerre civile qui frappa l’Espagne de 1936 à 1939, Madrid fut le théatre de nombreuses péripéties. En effet, s’agissant tout d’abord de la capitale, ce fut là que débuta le conflit, après une tentative de coup d’Etat orchestré par le général Franco. </p>
<p>Le putsch ayant échoué, le conflit s’étendit sur tout le pays, opposant les républicains aux armées de Franco.Prendre la capitale était essentiel pour les putschistes, puisque la contrôler signifiait gagner la guerre civile. Ce ne fut cependant pas une mince affaire, puisque les forces républicaines de Madrid parvinrent à résister jusqu’en 1939. Plusieurs assauts eurent lieux entre juillet 1936 et mars 1937, mais toutes échouèrent, les troupes militaires étant stoppées par les républicains. De plus Franco préféra se concentrer sur la prise de l’Alcazar de Tolède, un palais fortifié qui était alors réputé pour montrer une forte résistance. </p>
<p>Voyant que Madrid ne céderait pas face aux attaques frontales, Franco décida alors de faire une “guerre d’usure”, en prenant position tout autour de la capitale, bloquant ainsi l’accès aux ressources extérieures. Les forces républicaines, incapables de se ravitailler en nourritures, en armes et en munitions, s’affaiblirent petit à petit, et Madrid tomba finalement le 28 mars 1939.</p>
